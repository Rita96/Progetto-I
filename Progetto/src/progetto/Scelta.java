package progetto;

public class Scelta 
{
    private String codS;
    private String testoS;
    
    public Scelta(String c, String s)
    {
        codS=c;
        testoS=s;
    }
    
    public String getCodS()
    {
        return codS;
    }
    
    public String getTestoS()
    {
        return testoS;
    }
    
}
